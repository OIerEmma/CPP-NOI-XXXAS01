#include<bits/stdc++.h>
using namespace std;
char x;
int main() {
    cin >> x;
    switch(x) {
        case 'a':
            cout << "apple"; break;
        case 'b':
            cout << "banana"; break;
        case 'c':
            cout << "cat"; break;
        default: cout << "no";
    }
    return 0;
}