#include <bits/stdc++.h>
using namespace std;

// 线性DP版本
int main() {
  int n;
  cin >> n;
  // 核心算法
  // 状态：dp[i] = 把 i 拆成完全平方数之和的最少个数
  // 初始化
  vector<int> dp(n + 1);
  dp[0] = 0;  // 和为0不需要选择任何数
  // 计算顺序
  for (int i = 1; i <= n; i++) {
    dp[i] = i;  // 兜底：全用 1² 凑出 i
    for (int j = 1; j * j <= i; j++)  // 只枚举完全平方数 j²
      dp[i] = min(dp[i], dp[i - j*j] + 1);  // 减去 j²，转移并取最小
  }
  // 结果
  cout << dp[n] << endl;
  return 0;
}

