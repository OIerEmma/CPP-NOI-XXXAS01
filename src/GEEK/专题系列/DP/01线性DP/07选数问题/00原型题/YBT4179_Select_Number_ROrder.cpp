#include <iostream>
#include <vector>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<long long> a(n), dp(n + 1, 0);
    vector<int> b(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    for (int i = 0; i < n; i++) cin >> b[i];
    // 状态定义：dp[i] 表示从第 i 个数开始至结尾的选数中的最大值
    // 初始化
    dp[n] = 0;
    // 计算顺序
    for (int i = n - 1; i >= 0; i--) {
        // dp[i] = max(dp[i + 1], dp[i + b[i]] + a[i]), max(i+b[i], i+1) <= n
        dp[i] = dp[i + 1];
        dp[i] = max(dp[i], dp[min(n, i + b[i])] + a[i]);
    }
    // 返回结果
    cout << dp[0] << endl;
    return 0;
}

