#include <iostream>
#include <vector>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<int> b(n + 1);
    vector<long long> a(n + 1), dp(n + 1, 0);
    for (int i = 1; i <= n; i++) cin >> a[i];
    for (int i = 1; i <= n; i++) cin >> b[i];
    // 状态定义：在i之前（不含i）已选出的下标集合中，满足跳跃约束的最大累计和
    // 初始化
    long long ans = 0;
    // 计算顺序
    for (int i = 1; i <= n; i++) {
        // 跳跃：选了 i 之后，下一跳至少到 i+b[i]
        if (i + b[i] <= n) dp[i + b[i]] = max(dp[i + b[i]], dp[i] + a[i]);
        // 传递：不选 i，把当前最优值传递给 i+1
        if (i + 1 <= n) dp[i + 1] = max(dp[i + 1], dp[i]);
        // 尝试把当前下标 i 作为“最后一个选中的下标”
        ans = max(ans, dp[i] + a[i]);
    }
    // 返回结果
    cout << ans << endl;
    return 0;
}

